package Day03_Variables;

public class PrimitiveDataTypes {

		/*
		 byte - whole numbers from -128 to +127
		 short - whole numbers from -32768 to +32767
		 int - whole numbers from -2,147,483,648 to +2,147,483,647
		 long - large whole numbers but tag with l or L  such as 1000l or 1000L
		 
		 */
	public static void main(String[] args) {
		
		byte length = 1;
		byte width = -4;
		
		short snum = 20000;
		
		int sint = 2_000_000_000;
	
		System.out.println(length);
		System.out.println(width);
		System.out.println();
		System.out.println(snum);
		System.out.println();
		System.out.println();
		System.out.println(sint);
	}
	
}
