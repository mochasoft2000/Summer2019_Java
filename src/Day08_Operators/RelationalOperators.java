package Day08_Operators;

public class RelationalOperators {

	public static void main(String[] args) {
		
		/*
		 RELATIONAL OPERATORS RETURNS BOOLEAN 
		 
		 >   ; greater then
		 >=  ; greater then OR equal
		 <   ;
		 <=  ;
		 ==  ; equal to
		 !=  ; not equal to
		 
		  		 
		 
		 
		 */
		
		System.out.println ( 10>9 ); // returned true
		System.out.println ( 10 >= 9); // returned true
		System.out.println( 10 <= 9);  // returned false
		
		
		
	}
	
	
	
	
	
}
